package freecodecampkotlin


/**
 * We can declare variables in kotlin by using var or val keyword
 *  -> we have to use var or val to declare variable
 *  -> var is mutable
 *  -> val is immutable
 *
 * Declaring type is optional in kotlin.
 *  -> to declare a type we have to use colon(:) after the variable and then the type
 *
 * to declare a variable null we should use variable type with a ? at end and then
 * set the value to null
 */
fun main() {

    var name: String? = null
    var name2: String = "Rashel 2"
    var name3 = "Rashel 3"
    val name4 = "Rashel 4"
}